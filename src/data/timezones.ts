// Helper function to get timezones grouped by region
export const getTimezonesByGroup = (): Record<string, TimezoneOption[]> => {
  const grouped: Record<string, TimezoneOption[]> = {};
  timezones.forEach(tz => {
    if (!grouped[tz.group]) {
      grouped[tz.group] = [];
    }
    grouped[tz.group].push(tz);
  });
  return grouped;
};

// Helper function to search timezones
export const searchTimezones = (query: string): TimezoneOption[] => {
  const lowerQuery = query.toLowerCase();
  return timezones.filter(tz => 
    tz.label.toLowerCase().includes(lowerQuery) ||
    tz.value.toLowerCase().includes(lowerQuery)
  );
};
